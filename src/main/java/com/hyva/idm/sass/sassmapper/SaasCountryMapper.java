package com.hyva.idm.sass.sassmapper;
import com.hyva.idm.sass.sassentities.SassCountry;
import com.hyva.idm.sass.sasspojo.SassCountryPojo;

public class SaasCountryMapper {
    public static SassCountry mapPojoToEntity(SassCountryPojo sassCountryPojo){
        SassCountry sassCountry= new SassCountry();
        sassCountry.setCountryId(sassCountryPojo.getCountryId());
        sassCountry.setCountryName(sassCountryPojo.getCountryName());
        sassCountry.setLocationId(sassCountryPojo.getLocationId());
        sassCountry.setFlag(sassCountryPojo.getFlag());
        sassCountry.setUseraccount_id(sassCountryPojo.getUseraccount_id());
        sassCountry.setStatus(sassCountryPojo.getStatus());
        return sassCountry;
    }
    public static SassCountryPojo mapEntityToPojo(SassCountry sassCountry){
        SassCountryPojo sassCountryPojo= new SassCountryPojo();
        sassCountryPojo.setCountryId(sassCountry.getCountryId());
        sassCountryPojo.setCountryName(sassCountry.getCountryName());
        sassCountryPojo.setLocationId(sassCountry.getLocationId());
        sassCountryPojo.setFlag(sassCountry.getFlag());
        sassCountryPojo.setUseraccount_id(sassCountry.getUseraccount_id());
        sassCountryPojo.setStatus(sassCountry.getStatus());
        return sassCountryPojo;
    }

}


