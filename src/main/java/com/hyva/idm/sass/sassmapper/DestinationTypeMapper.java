package com.hyva.idm.sass.sassmapper;

import com.hyva.idm.sass.sassentities.DestinationType;
import com.hyva.idm.sass.sasspojo.DestinationTypePojo;

import java.awt.*;

public class DestinationTypeMapper {
    public static DestinationType mapPojoToEntity(DestinationTypePojo destination){
      DestinationType type=new DestinationType();
        type.setDescription(destination.getDescription());
        type.setDestinationName(destination.getDestinationName());
        type.setDocType(destination.getDocType());
        type.setStatus(destination.getStatus());
        type.setId(destination.getId());
        return type;

    }
}
